package com.project.SEP_BE_EmployeeManagement.model;

/*
* CĐ: nghỉ chế độ
* L: nghỉ lễ
* TC: nghỉ tiêu chuẩn
* NT: nghỉ tuần
* H: làm hành chính
* KL, P_KL, KL_P, P: nghỉ cả ngày
* P_H, H_P, KL_H, H_KL: làm nửa ngày
* Ô: nghỉ ốm
* */
public enum ESign {
    CĐ,
    L,
    TC,
    NT,
    H,
    KL,
    P,
    H_KL,
    H_P,
    KL_H,
    KL_P,
    P_H,
    P_KL,
    Ô
    }
